void IMI_Write2ByteMask( U32 u32RegLoAddr, U16 u16Value , U16 u16Mask )
{
    IMI_WriteByteMask( u32RegLoAddr, ( U8 )u16Value, ( U8 ) u16Mask );
    IMI_WriteByteMask( u32RegLoAddr + 1, ( U8 )( u16Value >> 8 ), ( U8 )( u16Mask >> 8 ) );
}
void IMI_WriteByteMask( U32 u32Reg, U8 u8Value, U8 u8Mask )
{
    IMI_WriteByte( u32Reg, ( (IMI_ReadByte( u32Reg ) & ( ~u8Mask )) | ( u8Value & u8Mask ) ) );
}


