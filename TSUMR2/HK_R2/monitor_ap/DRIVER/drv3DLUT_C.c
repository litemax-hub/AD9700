DWORD u32ScBaseAddr = 0;
    TblBaseAddr = 0;
    for(sramSel=0; sramSel<8; sramSel++)
    {
        msWriteByte(u32ScBaseAddr + SC14_C1,  sramSel);  //sram selection

        for(sramAddr=0; sramAddr<t3DSRAMTblSize[sramSel]; sramAddr++)
        {

            msWrite2Byte(u32ScBaseAddr + SC14_D4,  sramAddr); //sram address
            value = t3DSRAMMTbl[0][TblBaseAddr+sramAddr];
            if (t3DHWGain[ColorMode] == e3dLutDelta8X && (t3DSRAMMTbl[0][u16Idx/8] & (1 <<(u16Idx % 8))))
                msWrite2Byte(u32ScBaseAddr+SC14_C2, calc2sComplement((WORD)(value*u8SwRatio )|0x200));
            else
            {
                msWrite2Byte(u32ScBaseAddr + SC14_C2, calc2sComplement( (WORD)(value*u8SwRatio ) )); //R channel data
            }
            value = t3DSRAMMTbl[1][TblBaseAddr+sramAddr];
            if (t3DHWGain[ColorMode] == e3dLutDelta8X && (t3DSRAMMTbl[1][(u16Idx+729)/8] & (1 <<((u16Idx+729) % 8))))
                msWrite2Byte(u32ScBaseAddr+SC14_C4, calc2sComplement((WORD)(value*u8SwRatio )|0x200));
            else
                msWrite2Byte(u32ScBaseAddr + SC14_C4,  calc2sComplement((WORD)(value*u8SwRatio))); //G channel data
            value = t3DSRAMMTbl[2][TblBaseAddr+sramAddr];
            if (t3DHWGain[ColorMode] == e3dLutDelta8X && (t3DSRAMMTbl[2][(u16Idx+729*2)/8] & (1 <<((u16Idx+729*2) % 8))))
                msWrite2Byte(u32ScBaseAddr+SC14_C6, calc2sComplement((WORD)(value*u8SwRatio )|0x200));
            else
                msWrite2Byte(u32ScBaseAddr + SC14_C6,  calc2sComplement((WORD)(value*u8SwRatio ))); //B channel data
            u16Idx++;
            //execute write command
            msWriteBit(u32ScBaseAddr + SC14_C1, 1, _BIT5);
        }
        TblBaseAddr += t3DSRAMTblSize[sramSel];
    }

