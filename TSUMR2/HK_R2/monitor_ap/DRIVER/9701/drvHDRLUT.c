         akms = ((MS_U64)ak[i]*ms[i])>>16;
         akms = ((MS_U64)akms * tk[i]);
         bkms = ((MS_U64)bk[i]*ms[i])>>16;
         bkms = ((MS_U64)bkms * tk[i]);

         new_mk[i] = akms;
         new_mk_nxt[i] = bkms;
