DWORD Mhal_CFD_GammaSampling(DWORD u32In, DWORD u32Gamma, WORD u16A, WORD u16B, WORD u16S,BYTE u8BitDepth,BYTE sMode)
{
// u16Gamma : 4.16
// u16GInv  : 4.16
// u16A : 1.15
// u16B : 0.16
// u16S : 6.10
    unsigned long long u32Th = 0, u32Out = 0;

    u32Th = u16B;
    if (u32In <= u32Th)
        return ( (( ((DWORD)u32In ) * u16S + (1<<9) )>> 10)*( ((1<<u8BitDepth) -1) ))>>(u8BitDepth);

    if(0 == sMode)
    {
        u32Out = ( (((newPow04(u32In, u32Gamma))*u16A)+(1<<14))>>15) - ((u16A - 0x8000) << 1);// * ( ((1<<u8BitDepth)-1)/(1<<u8BitDepth) ) ;
    }
    else
    {
        u32Out =  newPow04(u32In, u32Gamma) - ((u16A - 0x8000) << 1);// * ( ((1<<u8BitDepth)-1)/(1<<u8BitDepth) ) ;
    }
    return ( (u32Out*( ((1<<u8BitDepth) -1) ))>>(u8BitDepth) );
}

